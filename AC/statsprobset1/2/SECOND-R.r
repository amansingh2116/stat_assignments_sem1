#Suppose we take two coins (one with probability of head 0.3, and the
#other with probability of head 0.6) and toss them alternately. How would
#the relative frequencies of heads behave? First argue theoretically. Then
#check by simulating the tosses in R.
ab1=sample(c(0,1),1000,replace=T,prob=c(0.7,0.3))
ab2=sample(c(0,1),1000,replace=T,prob=c(0.4,0.6))
odd=seq(1,2000,2)
even=seq(2,2000,2)
ab3 = 1:2000  
z=1:2000
ab3[odd]=ab1
ab3[even]=ab2
plot(z,cumsum(ab3)/z,ty='l',col='red',ylim=c(0,1))
# theoretically it should converge to the mean of 0.3 and 0.6 that is 0.45, 
#because the independ odd and even outcomes are respectively suppossed to converge
# to their specified probabilities of head. Now if we are combining them then they
# should average out each other

